# right shift bitwise operator (>>)

n = 20
result = n >> 2    # 20 / 2*2  = 20 / 4   = 5
print(result) # 5

n = 16
result = n >> 3   # 16 / 2*2*2 = 16/8   = 2
print(result) # 2

n = 128
result = n >> 3  # 128 / 2*2*2 = 128/8  = 16
print(result) # 16

n = -10
result = n >> 1  # -10 / 2  = -5
print(result) # -5